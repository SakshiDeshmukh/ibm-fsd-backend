package com.assign.core.java;

public class OddEven {

	public static void main(String[] args) {
		//odd-even for a no.
		  int m = 5;
		  if(m % 2 ==0) {
			  System.out.println("m is even");
		  }
		  else {
			  System.out.println("m is odd");
		  }
		  //odd-even range
		  for(int i=1;i<20;i++) {
			  if(i %2==0) {
				  System.out.println(i+ "  ::is even");
			  }
			  else {
				  System.out.println(i+ " :: is odd");
			  }  
    
		  }	  
   }
}
	